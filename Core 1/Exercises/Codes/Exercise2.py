# Data Type Playground

my_name = str("Jessie")     # String
my_age = int(2)             # Integer
my_gwa = float(1.25)        # Float
is_student = bool(True)     # Boolean
my_fav_animes = ["Naruto", "One Piece", "Detective Conan"]  # List
colors = ["Violet", "Red", "Black"]   # List
nothing_here = None         # None


print("Name:", my_name)
print("Age:", my_age)
print("GWA:", my_gwa)
print("Are you a student?", is_student)
print("Favorite numbers:", my_fav_animes)
print("Favorite Colors:", colors)
print("Nothing here:", nothing_here)
